package com.languageFundamentalsMethods;

public class NRNAmethod {
	//  No Return Type - No Argument Method(NRNA)

	public static void main(String[] args) {
		//   No Return Type - No Argument Method
		System.out.println("Main Method Started");
		hello();
		
		NRNAmethod n = new NRNAmethod();
		n.show();

	}
	
	static void hello() {
		// This method contain No Argument And No return Type
		System.out.println("Hello");
	}
	
	void show() {
		// This method contain No Argument And No return Type
		System.out.println("Students");
		
	}

}
